        dominated, use_hint, consume_turn = run_round(answer, low, high, ressive_hint, use_hint)  # –ø—Ä–æ–≤–µ—Ä–∫–∞ —Ä–∞—É–Ω–¥–∞ –∏–≥—Ä—ã –ø—Ä–∏ –Ω–∞–ª–∏—á–∏–∏ –≤—Å–µ—Ö –≤–≤–æ–¥–Ω—ã—Ö

