        print("‚äû" * 23)
        print("üî• –í–∞—à–∞ –ø–æ–ø—ã—Ç–∫–∞ —Å–æ–∂–∂–µ–Ω–∞ üî• –±—É–¥—å—Ç–µ –≤–Ω–∏–º–∞—Ç–µ–ª—å–Ω—ã –ø—Ä–∏ –≤–≤–æ–¥–µ")
