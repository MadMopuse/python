        is_win, hint_used, attempt_consumed = run_round(secret, low, high, hint_allowed, hint_used)  # –∑–∞–ø—É—Å–∫ —Ä–∞—É–Ω–¥–∞ —Å–æ –≤—Å–µ–º–∏ –≤–≤–æ–¥–Ω—ã–º–∏

        if is_win:  # –µ—Å–ª–∏ –≤—ã–ø–æ–ª–Ω–µ–Ω—ã —É—Å–ª–æ–≤–∏—è –ø–æ–±–µ–¥—ã –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            print("–ü–æ–∑–¥—Ä–∞–≤–ª—è—é, –≤—ã –æ–¥–µ—Ä–∂–∞–ª–∏ –ø–æ–±–µ–¥—É –≤ –Ω–µ–ª—ë–≥–∫–æ–º –±–æ—é —Å –º–∞—à–∏–Ω–æ–π üëπ")
            print("üëè" * 23)
            return attempt_number  # —Å–∫–æ–ª—å–∫–æ –ø–æ–ø—ã—Ç–æ–∫ –∑–∞—Ç—Ä–∞—á–µ–Ω–æ –Ω–∞ —Ä–µ—à–µ–Ω–∏–µ

        if attempt_consumed:  # —Ñ–æ—Ä–º—É–ª–∞ —Ä–∞—Å—á—ë—Ç–∞ –ø–æ–ø—ã—Ç–æ–∫
            remaining -= 1
            attempt_number += 1

