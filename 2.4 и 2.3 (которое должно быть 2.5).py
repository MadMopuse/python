        print(f"–ü–æ–ø—ã—Ç–∫–∞ –Ω–æ–º–µ—Ä: {attempt_number}. –û—Å—Ç–∞–ª–æ—Å—å –ø–æ–ø—ã—Ç–æ–∫: {remaining}.")
        print("üçÑ" * 23 + "\n")

